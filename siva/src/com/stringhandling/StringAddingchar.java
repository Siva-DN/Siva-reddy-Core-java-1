package com.stringhandling;

public class StringAddingchar {

	public static void main(String[] args) {

		String s1 = "Hello world";

		String result = "";

		for (int i = 0; i < s1.length(); i++) {

			for (int j = 0; j < 2 && j < s1.length(); j++) {
				result = result + j;
			}
//			for(int)
		}
	}

}
