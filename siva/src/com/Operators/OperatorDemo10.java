package com.Operators;

//Bitwise operator is used for performance optimized purpose and update passwords encryption and decryption
//for boolean expression perform well also with single &,|
//Bitwise & shift Operators-->   >>,  <<,  <<<.
public class OperatorDemo10 {

	public static void main(String[] args) {

		int s1 = 76;
		int s2 = 2;

		System.out.println(s1 << s2);// 120
		System.out.println(s1 >> s2);// 19

	}

}
