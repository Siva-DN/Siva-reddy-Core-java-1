package com.languagefundamentals;

import java.math.BigInteger;
import java.math.BigDecimal;

public class StringObjectDatatypes {
	Integer a1 = 232789;
	Byte b1;
	Character c1;
	String s1 = "dude";

	BigInteger b2;
	BigDecimal d1;
	String s2 = "helo";

	public static void main(String[] args) {

		StringObjectDatatypes s = new StringObjectDatatypes();

		// every object default value ==null
		System.out.println(s.a1);
		System.out.println(s.b1);
		System.out.println(s.c1);
		System.out.println(s.s1);
		System.out.println(s.b2);
		System.out.println(s.d1);
		System.out.println(s.s2);
		System.out.println(s.s2);

	}

}
